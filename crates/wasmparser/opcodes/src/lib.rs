pub struct Opcode {
    pub name: &'static str,
    pub opcode: u8,
    pub subopcode: Option<u32>,
    pub proposal: &'static str,
    pub arity: Arity,
    pub immediates: &'static [Immediate],
}

pub enum Immediate {
    BlockType,
    BrTable,
    TableMvp,
    MemoryMvp,
    Depth,
    Func,
    Table,
    Tag,
    Type,
    ValType,
    TryTable,
    Local,
    Global,
    MemArg(u8),
    I32,
    I64,
    F32,
    F64,
    HeapType,
    Field,
    Data,
    Elem,
    BrOnCast,
    U32,
    Memory,
    V128,
    ShuffleLanes,
    Lane(u8),
    FenceFlags,
    Ordering,
}

pub enum Arity {
    Fixed(u8, u8),
    Block,
    Br,
    BrIf,
    BrTable,
    Catch,
    Else,
    End,
    Tag,
    Call,
    Return,
    CallIndirect,
    Delegate,
    CatchAll,
    TryTable,
}

include!(concat!(env!("OUT_DIR"), "/opcodes.rs"));
